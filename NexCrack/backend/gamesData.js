const getGameById = (id) => {
  return gamesData.find(game => game.id === id) || null;
};

const getRelatedGames = (gameId, count = 3) => {
  const currentGame = getGameById(gameId);
  if (!currentGame) return [];

  const scored = gamesData
    .filter(g => g.id !== gameId)
    .map(g => {
      const matchCount = g.genres.filter(genre =>
        currentGame.genres.includes(genre)
      ).length;

      return { game: g, score: matchCount };
    })
    .sort((a, b) => b.score - a.score);

  return scored.slice(0, count).map(s => s.game);
};

